char *ft_strncat(char *dest, char *src, int nb)
{
	int i;

	i = 0;
	while (i < nb && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (dest[i++] != '\0')
		dest[i] = '\0';
	return (dest);
}
